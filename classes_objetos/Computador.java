package classes_objetos;

public class Computador {

	String modelo;
	int qtdMemoria;
	float capacidadeDisco;
	float processador;
	
	public String getModelo() {
		return modelo;
	}
	public void setModelo(String modelo) {
		this.modelo = modelo;
	}
	public int getQtdMemoria() {
		return qtdMemoria;
	}
	public void setQtdMemoria(int qtdMemoria) {
		this.qtdMemoria = qtdMemoria;
	}
	public float getCapacidadeDisco() {
		return capacidadeDisco;
	}
	public void setCapacidadeDisco(float capacidadeDisco) {
		this.capacidadeDisco = capacidadeDisco;
	}
	public float getProcessador() {
		return processador;
	}
	public void setProcessador(float processador) {
		this.processador = processador;
	}

	
}
